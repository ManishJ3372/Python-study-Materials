#changing case of a string
# we can change case of a string by using the following 4 methods
#upper()-->to convert all characters to uppper case
#lower()-->to convert all characters to lower case
#swapcase()-->convert all characters to uppper character to lower all lower to upper
#title()-->to convert all characters to title
#capitalize()--> only first character will be converted

#upper
print("manish java".upper())

#lower
print("JAVA PYTHON".lower())

#swapcase
print("MANISH java".swapcase())

#title
print("java django".title())

#capitalize
print("python java".capitalize())
